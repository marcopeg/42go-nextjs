import type { Metadata } from "next";
import { Inter } from "next/font/google";
import { getAppName } from "@/lib/app-config"; // Renamed from @/lib/config
import type { AppName } from "../AppConfig"; // Updated import path
import { AppConfigProvider } from "@/components/AppConfigProvider";
import "./globals.css";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const appName: AppName | null = await getAppName();

  if (!appName) {
    return (
      <html lang="en">
        <head></head>
        <body>
          <main className="flex min-h-screen flex-col items-center justify-center p-24">
            <h1 className="text-3xl font-bold">404 - App Not Found</h1>
            <p className="mt-4">
              No valid application configuration was identified for this
              request.
            </p>
          </main>
        </body>
      </html>
    );
  }

  return (
    <html lang="en" data-app-name={appName}>
      <head></head>
      <body className={inter.className}>
        <AppConfigProvider>{children}</AppConfigProvider>
      </body>
    </html>
  );
}
